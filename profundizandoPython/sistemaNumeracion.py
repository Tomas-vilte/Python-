# Profundizando sistemas de numeracion
# Decimal

a = 10
print(f'a:{a}')

# Binario
b = 0b1010
print(f'b:{b}')

# Octal
c = 0o12
print(f'c:{c}')

# Hexadecimal
d = 0xA
print(f'd:{d}')

# Convertir un tipo entenro, incluyendo base
# Base decimal
e= int('23', 10)
print(f'e:{e}')

# Base binario
f= int('10111', 2)
print(f'f:{f}')

# Base octal
g= int('27', 8)
print(f'g:{g}')

# Base hexadecimal
h= int('17', 16)
print(f'h:{h}')

# Base 5
i = int('344', 5)
print(f'i:{i}')